        <p>مثال للتجربة:</p>
        <p>
            قال رسول اللَِّهِ صلَّى اللهُ عَليهِ وسلَّمَ: (قال اللهُ عزَّ وجل:
            (( من آذى لي وليا فقد بارزني بالحرب ))) . وهو من أحاديث - بل من أشهر
            أحاديث - "القوم" رضِي الله عنهم ؛ قال الله جل جلاله : {"{"}لا خوف
